class Prac{

public static void main (String argsp[]){

	for (int i=1;i<=6;i++)
	{
	for (int j=1;j<=i;j++)
	{
		System.out.print(" "+i);
	}	
	System.out.println(" ");
	}


	for (int a=6-1;a>=1;a--)
	{
	for (int b=1;b<=a;b++)
	{
		System.out.print(" "+b);
	}
	System.out.println();
	}
	
}
}