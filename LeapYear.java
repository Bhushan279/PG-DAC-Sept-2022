import java.util.*;

class LeapYear{
	public static void main (String args[]){
		Scanner scan = new Scanner (System.in);
		int a = scan.nextInt();
		int b = a%4;
		if(b==0)
			System.out.println("The given year is a leap year");
		else
		{
			System.out.println("The given year is NOT a leap year");
		}
		
		
		
		
		
		
	}
}